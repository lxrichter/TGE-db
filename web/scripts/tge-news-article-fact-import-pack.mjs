#!/usr/bin/env node

import fs from "node:fs/promises";
import path from "node:path";
import * as XLSX from "xlsx";

const DEFAULT_INPUT = path.resolve(
  "..",
  "source-data",
  "tge-news-article-fact-review-2026-tuned-v2",
  "article_fact_review_sample.xlsx"
);
const DEFAULT_OUT = path.resolve(
  "..",
  "source-data",
  "tge-news-article-fact-import-pack"
);
const EXTRACTION_METHOD = "local_markdown_regex_v1";

const REQUIRED_COLUMNS = [
  "review_decision",
  "review_note",
  "source_reference",
  "published_date",
  "fact_type_code",
  "field_name",
  "extracted_value",
  "normalized_value",
  "confidence_score",
  "fact_reason",
  "evidence_snippet",
  "fact_key",
];

const DECISION_ALIASES = new Map([
  ["", "pending"],
  ["accept", "accept"],
  ["accepted", "accept"],
  ["approve", "accept"],
  ["approved", "accept"],
  ["confirm", "accept"],
  ["confirmed", "accept"],
  ["yes", "accept"],
  ["y", "accept"],
  ["reject", "reject"],
  ["rejected", "reject"],
  ["no", "reject"],
  ["n", "reject"],
  ["false_positive", "reject"],
  ["false-positive", "reject"],
  ["unclear", "unclear"],
  ["unsure", "unclear"],
  ["maybe", "unclear"],
  ["needs_review", "unclear"],
  ["needs-review", "unclear"],
  ["needs_rule_change", "needs_rule_change"],
  ["needs-rule-change", "needs_rule_change"],
  ["rule_change", "needs_rule_change"],
  ["rule-change", "needs_rule_change"],
  ["rule", "needs_rule_change"],
]);

const OUTPUT_COLUMNS = [
  "fact_key",
  "source_reference",
  "archive_file_path",
  "published_date",
  "fact_type_code",
  "entity_type",
  "entity_label",
  "field_name",
  "extracted_value",
  "normalized_value",
  "unit_code",
  "evidence_snippet",
  "confidence_score",
  "fact_status_code",
  "fact_reason",
  "review_decision",
  "review_note",
  "review_sample_bucket",
  "article_title",
  "article_url",
  "extraction_method",
  "extraction_metadata",
];

function parseArgs(argv) {
  const args = {
    input: process.env.TGE_ARTICLE_FACT_REVIEW_INPUT || DEFAULT_INPUT,
    out: process.env.TGE_ARTICLE_FACT_IMPORT_OUT || DEFAULT_OUT,
    acceptedOnly: false,
  };

  for (let index = 0; index < argv.length; index += 1) {
    const arg = argv[index];
    const next = argv[index + 1];

    if (arg === "--input" && next) {
      args.input = path.resolve(next);
      index += 1;
    } else if (arg === "--out" && next) {
      args.out = path.resolve(next);
      index += 1;
    } else if (arg === "--accepted-only") {
      args.acceptedOnly = true;
    } else if (arg === "--help" || arg === "-h") {
      printHelp();
      process.exit(0);
    }
  }

  args.input = path.resolve(args.input);
  args.out = path.resolve(args.out);

  return args;
}

function printHelp() {
  console.log(`
Usage:
  npm run tge-news:fact-import-pack -- --input "../source-data/.../article_fact_review_sample.xlsx"

Options:
  --input <file>    Reviewed CSV or XLSX generated by tge-news:facts.
  --out <dir>       Output directory. Defaults to ../source-data/tge-news-article-fact-import-pack.
  --accepted-only   Output confirmed/accepted candidates only.

Output:
  article_fact_candidates_reviewed_import.ndjson
  article_fact_candidates_reviewed_import.csv
  article_fact_candidates_confirmed.ndjson
  article_fact_candidates_rejected.ndjson
  article_fact_candidates_needs_review.ndjson
  article_fact_import_pack_summary.json

Privacy:
  Local-only. No database writes, no network calls, no full article body text.
`);
}

function parseCsv(text) {
  const rows = [];
  let row = [];
  let value = "";
  let inQuotes = false;

  for (let index = 0; index < text.length; index += 1) {
    const char = text[index];
    const next = text[index + 1];

    if (inQuotes) {
      if (char === '"' && next === '"') {
        value += '"';
        index += 1;
      } else if (char === '"') {
        inQuotes = false;
      } else {
        value += char;
      }
    } else if (char === '"') {
      inQuotes = true;
    } else if (char === ",") {
      row.push(value);
      value = "";
    } else if (char === "\n") {
      row.push(value);
      rows.push(row);
      row = [];
      value = "";
    } else if (char !== "\r") {
      value += char;
    }
  }

  if (value || row.length) {
    row.push(value);
    rows.push(row);
  }

  return recordsFromRows(rows);
}

function recordsFromRows(rows) {
  if (!rows.length) {
    return { columns: [], records: [] };
  }

  const [columns, ...dataRows] = rows;
  const normalizedColumns = columns.map((column) => String(column || "").trim());
  const records = dataRows
    .filter((dataRow) => dataRow.some((cell) => String(cell || "").trim()))
    .map((dataRow) =>
      Object.fromEntries(
        normalizedColumns.map((column, columnIndex) => [
          column,
          dataRow[columnIndex] === undefined ? "" : String(dataRow[columnIndex]),
        ])
      )
    );

  return { columns: normalizedColumns, records };
}

function readXlsx(input) {
  const readFile = XLSX.readFile || XLSX.default?.readFile;
  const workbook = readFile(input, { cellDates: false });
  const worksheet =
    workbook.Sheets["Review Sample"] ||
    workbook.Sheets[workbook.SheetNames[0]];

  if (!worksheet) {
    return { columns: [], records: [] };
  }

  return recordsFromRows(
    XLSX.utils.sheet_to_json(worksheet, {
      header: 1,
      defval: "",
      raw: false,
    })
  );
}

async function readReviewInput(input) {
  if (input.toLowerCase().endsWith(".xlsx")) {
    return readXlsx(input);
  }

  const text = await fs.readFile(input, "utf8");

  return parseCsv(text);
}

function normalizeDecision(value) {
  const key = String(value || "")
    .trim()
    .toLowerCase()
    .replace(/\s+/g, "_");

  return DECISION_ALIASES.get(key) || "invalid";
}

function statusForDecision(decision) {
  if (decision === "accept") {
    return "confirmed";
  }

  if (decision === "reject") {
    return "rejected";
  }

  if (decision === "unclear" || decision === "needs_rule_change") {
    return "needs_review";
  }

  return null;
}

function safeJsonParse(value, fallback = {}) {
  if (value && typeof value === "object") {
    return value;
  }

  const text = String(value || "").trim();

  if (!text) {
    return fallback;
  }

  try {
    return JSON.parse(text);
  } catch {
    return fallback;
  }
}

function csvEscape(value) {
  const text = value === null || value === undefined ? "" : String(value);

  if (/[",\n\r]/.test(text)) {
    return `"${text.replace(/"/g, '""')}"`;
  }

  return text;
}

function toCsv(rows) {
  const lines = [OUTPUT_COLUMNS.join(",")];

  for (const row of rows) {
    lines.push(
      OUTPUT_COLUMNS.map((column) => {
        const value =
          column === "normalized_value" || column === "extraction_metadata"
            ? JSON.stringify(row[column] || {})
            : row[column];

        return csvEscape(value);
      }).join(",")
    );
  }

  return `${lines.join("\n")}\n`;
}

function countBy(rows, key) {
  const counts = new Map();

  for (const row of rows) {
    const value = row[key] || "unknown";
    counts.set(value, (counts.get(value) || 0) + 1);
  }

  return Object.fromEntries(
    [...counts.entries()].sort(([a], [b]) => String(a).localeCompare(String(b)))
  );
}

function buildCandidate(record, inputFile) {
  const reviewDecision = normalizeDecision(record.review_decision);
  const factStatusCode = statusForDecision(reviewDecision);
  const normalizedValue = safeJsonParse(record.normalized_value);
  const confidenceScore = Number(record.confidence_score || 0);

  if (!factStatusCode || !Number.isFinite(confidenceScore)) {
    return null;
  }

  return {
    fact_key: record.fact_key,
    source_reference: record.source_reference,
    archive_file_path: record.archive_file_path || null,
    published_date: record.published_date || null,
    fact_type_code: record.fact_type_code,
    entity_type: record.entity_type || null,
    entity_label: record.entity_label || null,
    field_name: record.field_name || null,
    extracted_value: record.extracted_value,
    normalized_value: normalizedValue,
    unit_code: record.unit_code || null,
    evidence_snippet: record.evidence_snippet || null,
    confidence_score: confidenceScore,
    fact_status_code: factStatusCode,
    fact_reason: record.fact_reason || null,
    review_decision: reviewDecision,
    review_note: record.review_note || null,
    review_sample_bucket: record.review_sample_bucket || null,
    article_title: record.article_title || null,
    article_url: record.article_url || null,
    extraction_method: EXTRACTION_METHOD,
    extraction_metadata: {
      title: record.article_title || null,
      url: record.article_url || null,
      review_decision: reviewDecision,
      review_note: record.review_note || null,
      review_sample_bucket: record.review_sample_bucket || null,
      reviewed_from_file: path.basename(inputFile),
      body_text_stored: false,
      article_body_exported: false,
      entity_fields_updated: false,
      database_write_performed: false,
    },
  };
}

async function writeNdjson(filePath, rows) {
  await fs.writeFile(
    filePath,
    rows.map((row) => JSON.stringify(row)).join("\n") + "\n",
    "utf8"
  );
}

async function main() {
  const args = parseArgs(process.argv.slice(2));
  const { columns, records } = await readReviewInput(args.input);
  const missingColumns = REQUIRED_COLUMNS.filter((column) => !columns.includes(column));

  if (missingColumns.length) {
    throw new Error(`Missing required review columns: ${missingColumns.join(", ")}`);
  }

  const pendingRows = [];
  const invalidRows = [];
  const reviewedRows = [];

  for (const record of records) {
    const decision = normalizeDecision(record.review_decision);

    if (decision === "pending") {
      pendingRows.push(record);
      continue;
    }

    if (decision === "invalid") {
      invalidRows.push(record);
      continue;
    }

    if (args.acceptedOnly && decision !== "accept") {
      continue;
    }

    const candidate = buildCandidate(record, args.input);

    if (candidate) {
      reviewedRows.push(candidate);
    }
  }

  const confirmedRows = reviewedRows.filter(
    (row) => row.fact_status_code === "confirmed"
  );
  const rejectedRows = reviewedRows.filter(
    (row) => row.fact_status_code === "rejected"
  );
  const needsReviewRows = reviewedRows.filter(
    (row) => row.fact_status_code === "needs_review"
  );
  const summary = {
    generated_at: new Date().toISOString(),
    input_file: args.input,
    output_directory: args.out,
    mode: args.acceptedOnly ? "accepted_only" : "reviewed_candidates",
    privacy: {
      local_only: true,
      network_calls: false,
      database_writes: false,
      full_article_body_output: false,
    },
    counts: {
      input_rows: records.length,
      pending_rows: pendingRows.length,
      invalid_rows: invalidRows.length,
      reviewed_output_rows: reviewedRows.length,
      confirmed_rows: confirmedRows.length,
      rejected_rows: rejectedRows.length,
      needs_review_rows: needsReviewRows.length,
      by_fact_status: countBy(reviewedRows, "fact_status_code"),
      by_fact_type: countBy(reviewedRows, "fact_type_code"),
      by_field_name: countBy(reviewedRows, "field_name"),
    },
  };

  await fs.mkdir(args.out, { recursive: true });
  await writeNdjson(
    path.join(args.out, "article_fact_candidates_reviewed_import.ndjson"),
    reviewedRows
  );
  await fs.writeFile(
    path.join(args.out, "article_fact_candidates_reviewed_import.csv"),
    toCsv(reviewedRows),
    "utf8"
  );
  await writeNdjson(
    path.join(args.out, "article_fact_candidates_confirmed.ndjson"),
    confirmedRows
  );
  await writeNdjson(
    path.join(args.out, "article_fact_candidates_rejected.ndjson"),
    rejectedRows
  );
  await writeNdjson(
    path.join(args.out, "article_fact_candidates_needs_review.ndjson"),
    needsReviewRows
  );
  await fs.writeFile(
    path.join(args.out, "article_fact_import_pack_summary.json"),
    `${JSON.stringify(summary, null, 2)}\n`,
    "utf8"
  );

  console.log(
    JSON.stringify(
      {
        reviewed_output_rows: summary.counts.reviewed_output_rows,
        confirmed_rows: summary.counts.confirmed_rows,
        rejected_rows: summary.counts.rejected_rows,
        needs_review_rows: summary.counts.needs_review_rows,
        pending_rows: summary.counts.pending_rows,
        invalid_rows: summary.counts.invalid_rows,
        output_directory: summary.output_directory,
        privacy: summary.privacy,
      },
      null,
      2
    )
  );
}

main().catch((error) => {
  console.error(error);
  process.exit(1);
});
