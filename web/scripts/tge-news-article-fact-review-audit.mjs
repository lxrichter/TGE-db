#!/usr/bin/env node

import fs from "node:fs/promises";
import path from "node:path";
import * as XLSX from "xlsx";

const DEFAULT_INPUT = path.resolve(
  "..",
  "source-data",
  "tge-news-article-fact-review-2026-focused",
  "article_fact_review_sample.csv"
);

const REQUIRED_COLUMNS = [
  "review_decision",
  "review_note",
  "source_reference",
  "fact_type_code",
  "field_name",
  "extracted_value",
  "confidence_score",
  "fact_reason",
  "fact_key",
];

const DECISION_ALIASES = new Map([
  ["", "pending"],
  ["accept", "accept"],
  ["accepted", "accept"],
  ["approve", "accept"],
  ["approved", "accept"],
  ["confirm", "accept"],
  ["confirmed", "accept"],
  ["yes", "accept"],
  ["y", "accept"],
  ["reject", "reject"],
  ["rejected", "reject"],
  ["no", "reject"],
  ["n", "reject"],
  ["false_positive", "reject"],
  ["false-positive", "reject"],
  ["unclear", "unclear"],
  ["unsure", "unclear"],
  ["maybe", "unclear"],
  ["needs_review", "unclear"],
  ["needs-review", "unclear"],
  ["needs_rule_change", "needs_rule_change"],
  ["needs-rule-change", "needs_rule_change"],
  ["rule_change", "needs_rule_change"],
  ["rule-change", "needs_rule_change"],
  ["rule", "needs_rule_change"],
]);

function parseArgs(argv) {
  const args = {
    input: process.env.TGE_ARTICLE_FACT_REVIEW_CSV || DEFAULT_INPUT,
    out: "",
  };

  for (let index = 0; index < argv.length; index += 1) {
    const arg = argv[index];
    const next = argv[index + 1];

    if (arg === "--input" && next) {
      args.input = path.resolve(next);
      index += 1;
    } else if (arg === "--out" && next) {
      args.out = path.resolve(next);
      index += 1;
    } else if (arg === "--help" || arg === "-h") {
      printHelp();
      process.exit(0);
    }
  }

  args.input = path.resolve(args.input);
  args.out = args.out || path.dirname(args.input);

  return args;
}

function printHelp() {
  console.log(`
Usage:
  npm run tge-news:fact-review -- --input "../source-data/.../article_fact_review_sample.csv"

Options:
  --input <file> Review CSV or XLSX generated by tge-news:facts.
  --out <dir>    Output directory. Defaults to the input file directory.

Review decisions:
  accept, reject, unclear, needs_rule_change, or blank/pending.

Output:
  article_fact_review_audit.json
  article_fact_review_audit.md
  article_fact_review_rule_notes.csv
`);
}

function parseCsv(text) {
  const rows = [];
  let row = [];
  let value = "";
  let inQuotes = false;

  for (let index = 0; index < text.length; index += 1) {
    const char = text[index];
    const next = text[index + 1];

    if (inQuotes) {
      if (char === '"' && next === '"') {
        value += '"';
        index += 1;
      } else if (char === '"') {
        inQuotes = false;
      } else {
        value += char;
      }
    } else if (char === '"') {
      inQuotes = true;
    } else if (char === ",") {
      row.push(value);
      value = "";
    } else if (char === "\n") {
      row.push(value);
      rows.push(row);
      row = [];
      value = "";
    } else if (char !== "\r") {
      value += char;
    }
  }

  if (value || row.length) {
    row.push(value);
    rows.push(row);
  }

  if (!rows.length) {
    return { columns: [], records: [] };
  }

  const [columns, ...dataRows] = rows;
  const records = dataRows
    .filter((dataRow) => dataRow.some((cell) => cell.trim()))
    .map((dataRow) =>
      Object.fromEntries(
        columns.map((column, columnIndex) => [column, dataRow[columnIndex] || ""])
      )
    );

  return { columns, records };
}

function recordsFromRows(rows) {
  if (!rows.length) {
    return { columns: [], records: [] };
  }

  const [columns, ...dataRows] = rows;
  const normalizedColumns = columns.map((column) => String(column || "").trim());
  const records = dataRows
    .filter((dataRow) => dataRow.some((cell) => String(cell || "").trim()))
    .map((dataRow) =>
      Object.fromEntries(
        normalizedColumns.map((column, columnIndex) => [
          column,
          dataRow[columnIndex] === undefined ? "" : String(dataRow[columnIndex]),
        ])
      )
    );

  return { columns: normalizedColumns, records };
}

function readXlsx(input) {
  const readFile = XLSX.readFile || XLSX.default?.readFile;
  const workbook = readFile(input, { cellDates: false });
  const worksheet =
    workbook.Sheets["Review Sample"] ||
    workbook.Sheets[workbook.SheetNames[0]];

  if (!worksheet) {
    return { columns: [], records: [] };
  }

  return recordsFromRows(
    XLSX.utils.sheet_to_json(worksheet, {
      header: 1,
      defval: "",
      raw: false,
    })
  );
}

async function readReviewInput(input) {
  if (input.toLowerCase().endsWith(".xlsx")) {
    return readXlsx(input);
  }

  const text = await fs.readFile(input, "utf8");

  return parseCsv(text);
}

function csvEscape(value) {
  const text = value === null || value === undefined ? "" : String(value);

  if (/[",\n\r]/.test(text)) {
    return `"${text.replace(/"/g, '""')}"`;
  }

  return text;
}

function normalizeDecision(value) {
  const key = String(value || "")
    .trim()
    .toLowerCase()
    .replace(/\s+/g, "_");

  return DECISION_ALIASES.get(key) || "invalid";
}

function increment(map, key, amount = 1) {
  map.set(key, (map.get(key) || 0) + amount);
}

function countMapToObject(map) {
  return Object.fromEntries(
    [...map.entries()].sort(([a], [b]) => String(a).localeCompare(String(b)))
  );
}

function createBucket() {
  return {
    total: 0,
    pending: 0,
    accept: 0,
    reject: 0,
    unclear: 0,
    needs_rule_change: 0,
    invalid: 0,
  };
}

function updateBucket(bucket, decision) {
  bucket.total += 1;
  bucket[decision] = (bucket[decision] || 0) + 1;
}

function bucketReviewStats(bucket) {
  const reviewed =
    bucket.accept + bucket.reject + bucket.unclear + bucket.needs_rule_change;
  const acceptRate = reviewed ? bucket.accept / reviewed : null;
  const ruleConcernRate = reviewed
    ? (bucket.reject + bucket.needs_rule_change) / reviewed
    : null;

  return {
    ...bucket,
    reviewed,
    accept_rate_reviewed: acceptRate === null ? null : Number(acceptRate.toFixed(4)),
    rule_concern_rate_reviewed:
      ruleConcernRate === null ? null : Number(ruleConcernRate.toFixed(4)),
  };
}

function summarize(records) {
  const decisionCounts = new Map();
  const invalidDecisionValues = new Map();
  const byFactType = new Map();
  const byFieldName = new Map();
  const bySampleBucket = new Map();
  const ruleNoteRows = [];

  for (const record of records) {
    const decision = normalizeDecision(record.review_decision);
    const factType = record.fact_type_code || "unknown";
    const fieldName = record.field_name || "unknown";
    const sampleBucket = record.review_sample_bucket || "not_recorded";

    increment(decisionCounts, decision);

    if (!byFactType.has(factType)) {
      byFactType.set(factType, createBucket());
    }

    if (!byFieldName.has(fieldName)) {
      byFieldName.set(fieldName, createBucket());
    }

    if (!bySampleBucket.has(sampleBucket)) {
      bySampleBucket.set(sampleBucket, createBucket());
    }

    updateBucket(byFactType.get(factType), decision);
    updateBucket(byFieldName.get(fieldName), decision);
    updateBucket(bySampleBucket.get(sampleBucket), decision);

    if (decision === "invalid") {
      increment(invalidDecisionValues, record.review_decision || "(blank)");
    }

    if (
      decision === "reject" ||
      decision === "unclear" ||
      decision === "needs_rule_change"
    ) {
      ruleNoteRows.push({
        decision,
        review_note: record.review_note,
        fact_type_code: record.fact_type_code,
        field_name: record.field_name,
        extracted_value: record.extracted_value,
        confidence_score: record.confidence_score,
        review_sample_bucket: record.review_sample_bucket,
        fact_reason: record.fact_reason,
        source_reference: record.source_reference,
        article_title: record.article_title,
        archive_file_path: record.archive_file_path,
        fact_key: record.fact_key,
      });
    }
  }

  const factTypeSummary = Object.fromEntries(
    [...byFactType.entries()]
      .map(([key, bucket]) => [key, bucketReviewStats(bucket)])
      .sort(([, a], [, b]) => {
        const concernDiff =
          (b.rule_concern_rate_reviewed || 0) - (a.rule_concern_rate_reviewed || 0);

        return concernDiff || b.reviewed - a.reviewed || b.total - a.total;
      })
  );
  const fieldSummary = Object.fromEntries(
    [...byFieldName.entries()]
      .map(([key, bucket]) => [key, bucketReviewStats(bucket)])
      .sort(([, a], [, b]) => {
        const concernDiff =
          (b.rule_concern_rate_reviewed || 0) - (a.rule_concern_rate_reviewed || 0);

        return concernDiff || b.reviewed - a.reviewed || b.total - a.total;
      })
  );
  const bucketSummary = Object.fromEntries(
    [...bySampleBucket.entries()]
      .map(([key, bucket]) => [key, bucketReviewStats(bucket)])
      .sort(([a], [b]) => String(a).localeCompare(String(b)))
  );

  const totals = bucketReviewStats(
    records.reduce((bucket, record) => {
      updateBucket(bucket, normalizeDecision(record.review_decision));
      return bucket;
    }, createBucket())
  );

  return {
    totals,
    decisions: countMapToObject(decisionCounts),
    invalid_decision_values: countMapToObject(invalidDecisionValues),
    by_fact_type: factTypeSummary,
    by_field_name: fieldSummary,
    by_sample_bucket: bucketSummary,
    rule_note_rows: ruleNoteRows,
  };
}

function toRuleNotesCsv(rows) {
  const columns = [
    "decision",
    "review_note",
    "fact_type_code",
    "field_name",
    "extracted_value",
    "confidence_score",
    "review_sample_bucket",
    "fact_reason",
    "source_reference",
    "article_title",
    "archive_file_path",
    "fact_key",
  ];
  const lines = [columns.join(",")];

  for (const row of rows) {
    lines.push(columns.map((column) => csvEscape(row[column])).join(","));
  }

  return `${lines.join("\n")}\n`;
}

function markdownTable(headers, rows) {
  const headerLine = `| ${headers.join(" | ")} |`;
  const separatorLine = `| ${headers.map(() => "---").join(" | ")} |`;
  const rowLines = rows.map(
    (row) => `| ${row.map((cell) => String(cell ?? "")).join(" | ")} |`
  );

  return [headerLine, separatorLine, ...rowLines].join("\n");
}

function percent(value) {
  if (value === null || value === undefined) {
    return "";
  }

  return `${Math.round(value * 100)}%`;
}

function createMarkdownReport({ input, generatedAt, summary }) {
  const factTypeRows = Object.entries(summary.by_fact_type).map(([type, stats]) => [
    type,
    stats.total,
    stats.reviewed,
    stats.accept,
    stats.reject,
    stats.unclear,
    stats.needs_rule_change,
    percent(stats.accept_rate_reviewed),
    percent(stats.rule_concern_rate_reviewed),
  ]);
  const fieldRows = Object.entries(summary.by_field_name)
    .slice(0, 20)
    .map(([field, stats]) => [
      field,
      stats.total,
      stats.reviewed,
      stats.accept,
      stats.reject,
      stats.unclear,
      stats.needs_rule_change,
      percent(stats.accept_rate_reviewed),
      percent(stats.rule_concern_rate_reviewed),
    ]);
  const bucketRows = Object.entries(summary.by_sample_bucket).map(
    ([bucket, stats]) => [
      bucket,
      stats.total,
      stats.reviewed,
      stats.accept,
      stats.reject,
      stats.unclear,
      stats.needs_rule_change,
      percent(stats.accept_rate_reviewed),
      percent(stats.rule_concern_rate_reviewed),
    ]
  );

  return `# Article Fact Review Audit

Generated: ${generatedAt}

Input CSV: \`${input}\`

## Review Status

- Total rows: ${summary.totals.total}
- Reviewed rows: ${summary.totals.reviewed}
- Pending rows: ${summary.totals.pending}
- Accepted rows: ${summary.totals.accept}
- Rejected rows: ${summary.totals.reject}
- Unclear rows: ${summary.totals.unclear}
- Needs rule change rows: ${summary.totals.needs_rule_change}
- Invalid decision rows: ${summary.totals.invalid}

## By Fact Type

${markdownTable(
  [
    "Fact type",
    "Rows",
    "Reviewed",
    "Accept",
    "Reject",
    "Unclear",
    "Rule change",
    "Accept rate",
    "Concern rate",
  ],
  factTypeRows
)}

## By Field

${markdownTable(
  [
    "Field",
    "Rows",
    "Reviewed",
    "Accept",
    "Reject",
    "Unclear",
    "Rule change",
    "Accept rate",
    "Concern rate",
  ],
  fieldRows
)}

## By Review Sample Bucket

${markdownTable(
  [
    "Bucket",
    "Rows",
    "Reviewed",
    "Accept",
    "Reject",
    "Unclear",
    "Rule change",
    "Accept rate",
    "Concern rate",
  ],
  bucketRows
)}

## Interpretation

Use this audit after manually marking \`review_decision\` in the sample CSV.
Rows marked \`reject\`, \`unclear\`, or \`needs_rule_change\` are copied into
\`article_fact_review_rule_notes.csv\` for rule tuning. No database records are
created or modified by this audit.
`;
}

async function main() {
  const args = parseArgs(process.argv.slice(2));
  const { columns, records } = await readReviewInput(args.input);
  const missingColumns = REQUIRED_COLUMNS.filter((column) => !columns.includes(column));

  if (missingColumns.length) {
    throw new Error(`Missing required review CSV columns: ${missingColumns.join(", ")}`);
  }

  const generatedAt = new Date().toISOString();
  const summary = summarize(records);
  const audit = {
    generated_at: generatedAt,
    input_csv: args.input,
    privacy: {
      local_only: true,
      network_calls: false,
      database_writes: false,
      full_article_body_output: false,
    },
    ...summary,
  };

  await fs.mkdir(args.out, { recursive: true });
  await fs.writeFile(
    path.join(args.out, "article_fact_review_audit.json"),
    `${JSON.stringify(audit, null, 2)}\n`,
    "utf8"
  );
  await fs.writeFile(
    path.join(args.out, "article_fact_review_audit.md"),
    createMarkdownReport({
      input: args.input,
      generatedAt,
      summary,
    }),
    "utf8"
  );
  await fs.writeFile(
    path.join(args.out, "article_fact_review_rule_notes.csv"),
    toRuleNotesCsv(summary.rule_note_rows),
    "utf8"
  );

  console.log(
    JSON.stringify(
      {
        rows: summary.totals.total,
        reviewed: summary.totals.reviewed,
        pending: summary.totals.pending,
        decisions: summary.decisions,
        invalid_decision_values: summary.invalid_decision_values,
        output_directory: args.out,
        privacy: audit.privacy,
      },
      null,
      2
    )
  );
}

main().catch((error) => {
  console.error(error);
  process.exit(1);
});
