DROP TABLE IF EXISTS plants;
DROP TABLE IF EXISTS projects;

CREATE TABLE plants (
  id INTEGER PRIMARY KEY AUTOINCREMENT,
  plant_name TEXT,
  project_group TEXT,
  other_name TEXT,
  owner_operator TEXT,
  developer TEXT,
  location_text TEXT,
  country TEXT,
  region TEXT,
  wb_region TEXT,
  potential_min_mw REAL,
  potential_max_mw REAL,
  installed_capacity_mw REAL,
  capacity_running_mw REAL,
  gross_production_gwh REAL,
  start_dev_year TEXT,
  cod TEXT,
  resource_type TEXT,
  resource_temp_c REAL,
  project_phase TEXT,
  phase_historical TEXT,
  field_name TEXT,
  wells_total INTEGER,
  wells_prod_active INTEGER,
  wells_reinj_active INTEGER,
  wells_inactive_standby INTEGER,
  wells_other_exploration INTEGER,
  well_depth_prod_m REAL,
  temp_prod_well_c REAL,
  flow_rate_ls REAL,
  number_of_unit TEXT,
  plant_technology TEXT,
  turbine_supplier TEXT,
  epc_suppliers TEXT,
  investor TEXT,
  ppa_usd_kwh REAL,
  total_investment_cost TEXT,
  notes TEXT,
  location_x REAL,
  location_y REAL,
  website_information TEXT,
  date_created TEXT,
  date_edited TEXT,
  edited_description TEXT,
  research_status TEXT
);

